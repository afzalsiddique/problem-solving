def deleteNode(head, position):
    if position==0:#head position
        head = head.next
        return head
    else:#position other than head
        current = head
        for i in range(position-1):
            current = current.next
        if current.next.next == None: #tail position
            current.next = None
            return head
        else:#other than head or tail
            current.next =current.next.next
            return head