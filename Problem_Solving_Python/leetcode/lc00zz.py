class Solution:
    def maxAreaOfIsland(self, grid: List[List[int]]) -> int:
        m,n=len(grid),len(grid[0])

        par={(i,j):(i,j) for i in range(m) for j in range(n)}
        def find(a):
            if par[a]==a:
                return a
            return find(par[a])
        def union(a,b):
            par[a]=find(b)

        def valid(i,j):
            return 0<=i<m and 0<=j<n
        def include_neigh(i,j):
            if grid[i][j]==0: return
            for di,dj in [(1,0),(0,1),(-1,0),(0,-1)]:
                newI,newJ=i+di,j+dj
                if valid(newI,newJ) and grid[newI][newJ]==1:
                    union((i,j),(newI,newJ))

        for i in range(m):
            for j in range(n):
                include_neigh(i,j)

        di=defaultdict(int)
        for i in range(m):
            for j in range(n):
                if grid[i][j]==1:
                    di[find((i,j))]+=1
        return max(di.values()) if di else 0

